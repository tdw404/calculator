public class Calculator{
    public double add(double a, double b){
        double mathAnswer;
        mathAnswer = a + b;
        return mathAnswer;
    }

    public double subtract(double a, double b){
        double mathAnswer;
        mathAnswer = a - b;
        return mathAnswer;
    }

    public double multiply(double a, double b){
        double mathAnswer;
        mathAnswer = a * b;
        return mathAnswer;
    }

    public double divide(double a, double b){
        double mathAnswer;
        mathAnswer = a / b;
        return mathAnswer;
    }
}